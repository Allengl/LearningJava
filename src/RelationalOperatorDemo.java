public class RelationalOperatorDemo {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        boolean isTrue = a < b; // true
        boolean isFalse = a > b; // false
        boolean isTrue2 = a <= b; // true
        boolean isFalse2 = a >= b; // false
        boolean isTrue3 = a == b; // false
        boolean isFalse3 = a != b; // true
        System.out.println(isTrue);
        System.out.println(isFalse);
        System.out.println(isTrue2);
        System.out.println(isFalse2);
        System.out.println(isTrue3);
        System.out.println(isFalse3);
    }
}
